/*
List - coleçao ordenada e pode ser dupliacado
    ArrayList - mantém a ordem e permite elementos duplicados, armazena e acessar dados
    LinkedList - Lista com inserção e remoção rápidas de elementos, manipular dados

Set(conjunto) - Colecao com elementos unicos
    HashSet - Coleção não ordenada de elementos únicos
    TreeSet - Conjunto ordenado de elementos únicos naturais
    LinkedHashSet - Mantém a ordem em que os elementos foram inseridos

Map - Pares de valores com chave unica
    HashMap - Armazena pares chave/valor sem ordem
    TreeMap - Mapa ordenado com base na ordem alfabetica ou numerica das chaves, não permite chave nula
    LinkedHashMap - Mantém a ordem em que as chaves foram inseridas
*/

